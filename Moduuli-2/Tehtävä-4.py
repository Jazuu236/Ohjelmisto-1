print("Syötä kolme kokonaislukua")
ensimmäinen = int(input("Ensimmäinen luku: "))
toinen = int(input("Toinen luku: "))
kolmas = int(input("Kolmas luku: "))
summa = ensimmäinen + toinen + kolmas
tulo = ensimmäinen * toinen * kolmas
keskiarvo = summa/3
print(f"Summa: {summa}, Tulo: {tulo}, Keskiarvo: {keskiarvo}")